package week6ACD;

public class Song {
		int length;
		String artist;
		String title;
		
		// song
		public int getLength() {
			return length;
		}
		public void setLength(int length) {
			this.length = length;
		}
		
		// artist
		public String getArtist() {
			return artist;
		}
		public void setArtist(String artist) {
			this.artist = artist;
		}
		
		//title
		public String getTitle() {
			return title;
		}
		public void setTitle(String title) {
			this.title = title;
		}
		
		
}
