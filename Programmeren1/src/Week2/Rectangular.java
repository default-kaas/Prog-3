package Week2;

public class Rectangular {
	
	public static void main(String [ ] args) {
		int counter = 0;
		
		while(counter <10) {
			System.out.print("-");
			counter++;
		}
		
		counter = 0;
		System.out.println();
		while(counter <5) {
			
			System.out.println("|        |");
			counter++;
		}
		
		counter = 0;
		
		while(counter <10) {
			System.out.print("-");
			counter++;
		}
		
		
	}
	
}
